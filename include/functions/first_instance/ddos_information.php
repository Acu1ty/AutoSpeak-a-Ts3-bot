class ddos_information
{
	private static $name;
	private static $cfg;
	
	static public function construct($event_name)
	{
		global $cfg;
		self::$cfg = $cfg[$event_name];
		self::$name = $event_name;
	}

	static public function before_clients()
	{
		global $query, $server_info;

		if($server_info['virtualserver_total_packetloss_total'] > self::$cfg['packet_loss'])
			$query->sendMessage(3, $server_info['virtualserver_id'], fread(fopen(self::$cfg['file'], "r"), filesize(self::$cfg['file'])));
	}
}
